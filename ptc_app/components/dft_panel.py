"""DFT comparison panel — PTC vs B3LYP head-to-head on identical molecules.

Two regimes :
  * Full benchmark : 860 mol, B3LYP/6-31G* (ZPE Scott-Radom 0.9806)
  * 3-bin segmented: 536 mol n≤12, 3-way PTC/def2-TZVP/6-31G* (geom + ZPE
    recycled from the 6-31G* run)

Data file regenerated by scripts/regenerate_dft_comparison_data.py.
"""
import streamlit as st
